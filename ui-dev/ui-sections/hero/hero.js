;(function(document,$) {


    window.sHero = window.sHero || {};

/////////////////////////////////////////////////////////////////////////////////////////////////////////
////// Section Hero - Init
/////////////////////////////////////////////////////////////////////////////////////////////////////////


    sHero.jsInit = function() {
       
         console.log('ki-hero.js');
         sHero.foldHeight();
         sHero.itemCount();
         
    };



/////////////////////////////////////////////////////////////////////////////////////////////////////////
////// Section Hero - height
/////////////////////////////////////////////////////////////////////////////////////////////////////////

    sHero.foldHeight = function () {


        $('.s-hero.js-height').each(function () {


            var sHolder = $('> div', this);
            var hHolder = $('> div', this).height();
           
            var wHeight = $(window).height();
            var scrollTop = $(window).scrollTop();

            // console.log('height' + + hHolder);
            // console.log('scrollTop' + + scrollTop);

            sHolder.height(wHeight);

            sHolder.height(wHeight);
            

            // Next section
            $('.s-colors').addClass('add').css('margin-top', wHeight);

        });


        setTimeout(function() {

            $('.s-hero').addClass('-animate');

        }, 300);

        


    };


/////////////////////////////////////////////////////////////////////////////////////////////////////////
////// Section Hero - Item count
/////////////////////////////////////////////////////////////////////////////////////////////////////////

    sHero.itemCount = function () {


        $('.s-hero .global').each(function () {

            var el = $(this);
            var elCount =  el.children().size();

            el.parent().addClass('-count-' + elCount);

        });

    };
  

/////////////////////////////////////////////////////////////////////////////////////////////////////////
////// Object + Enquire.js
/////////////////////////////////////////////////////////////////////////////////////////////////////////
    
     sHero.jsObject =  {

            objectInit: function() {
                  
                    // enquire.js
                    enquire.register("screen and (min-width:768px)", {
              
                            setup : function() {

                              console.log('jsObject setup');  
                            
                            },
                            match : function() {
                            
                                console.log('jsObject match');

                            },
                            unmatch : function() {

                                console.log('jsObject unmatch');

                            }

                    }, true); 


            }, // objectInit



/* - - - Mobile - - - */

            objectMobileClick: function() {
                    
            }, // objectMobileClick


            objectMobileClickReset: function() {
                   
            }, // objectMobileClickReset


            objectMobileDestroy: function() {
                   
            }, // objectMobileDestroy


     }; // sHero.jsObject END

/////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////

/////////////////////////////////////////////////////////////////////////////////////////////////////////
////// Load
/////////////////////////////////////////////////////////////////////////////////////////////////////////

    $(window).on('load', function(){
       
    });

/////////////////////////////////////////////////////////////////////////////////////////////////////////
////// Ready
/////////////////////////////////////////////////////////////////////////////////////////////////////////

    $(document).on('ready', function(){

        sHero.jsInit();
      
    });

/////////////////////////////////////////////////////////////////////////////////////////////////////////
////// Scroll
/////////////////////////////////////////////////////////////////////////////////////////////////////////

    $(document).on('scroll', function(){
             sHero.foldHeight();
    });

/////////////////////////////////////////////////////////////////////////////////////////////////////////
////// Resize
/////////////////////////////////////////////////////////////////////////////////////////////////////////

    // jquery.debouncing.js, thanks Paul Irish

    $(window).smartresize(function(){
        sHero.foldHeight();
    });

/////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////

})(document,jQuery);

